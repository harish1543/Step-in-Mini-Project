#include<stdio.h>

/**
 * @brief : sorts input array using insertion sort
 * 
 * @param arr : input array
 * @param n : size of input array
 */
void insertionSort(int arr[], int n)
{
    for (int i = 1; i < n; i++)
    {
        int key = arr[i];
        int j = i - 1;
 
        while (j >= 0 && arr[j] > key)
        {
            arr[j + 1] = arr[j];
            j = j - 1;
        }
        arr[j + 1] = key;
    }
}